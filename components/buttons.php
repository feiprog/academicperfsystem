function actionButton($type, $text, $action, $icon = null, $confirm = false) {
    $baseClasses = "inline-flex items-center px-4 py-2 rounded-lg font-medium transition-all duration-200 focus:outline-none focus:ring-2 focus:ring-offset-2";
    $iconClasses = $icon ? "mr-2 -ml-1" : "";
    
    $buttonTypes = [
        'primary' => "bg-sky-600 text-white hover:bg-sky-700 focus:ring-sky-500",
        'secondary' => "bg-gray-100 text-gray-700 hover:bg-gray-200 focus:ring-gray-500",
        'success' => "bg-green-600 text-white hover:bg-green-700 focus:ring-green-500",
        'danger' => "bg-red-600 text-white hover:bg-red-700 focus:ring-red-500",
        'warning' => "bg-yellow-500 text-white hover:bg-yellow-600 focus:ring-yellow-500",
        'info' => "bg-blue-600 text-white hover:bg-blue-700 focus:ring-blue-500"
    ];

    $classes = $baseClasses . " " . $buttonTypes[$type] . " " . $iconClasses;
    $onclick = $confirm ? "onclick=\"return confirm('Are you sure you want to proceed?')\"" : "";
    
    $button = "<button type=\"button\" class=\"$classes\" $onclick $action>";
    if ($icon) {
        $button .= "<svg class=\"w-5 h-5 $iconClasses\" fill=\"none\" stroke=\"currentColor\" viewBox=\"0 0 24 24\">$icon</svg>";
    }
    $button .= $text . "</button>";
    
    return $button;
}

